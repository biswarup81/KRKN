            <p>At the time of admission, a student is to pay the session Charge, Admission fee & one Month tuition fees. In addition, University Registration fee & Enrolment fee, as fixed by the University & College development fee are also to be paid at the time of admission.</p>
            <h4>Session</h4>
            <p>The Academic Session of the College starts from July and ends in June next year.</p>
            <h4>Procedure for Admission</h4>
            <p>Students seeking admission into different under Graduate Courses must have to apply in the prescribed form to be had from the College Office. The schedule for admission is put up on the notice board of the College & the admission process goes on according to the schedule. Students willing to apply in different Hons. Subjects must apply in different application forms. Students applying both Hons & general courses must submit separate application form.</p>

<p>The selection of students for admission to any General & Hons subjects is made strictly on the basis of merit. All sorts of information regarding admission & merit lists are published in the notice Board of the College. For any sort of dispute in admission procedure the decision of the authority is final.</p>

<ul>
    <li>22% seats are reserved for SC Candidates</li>
    <li>6% seats are reserved for ST</li>
    <li>3% seats are reserved for Physical Handicapped person</li>
</ul>

<h4>Discipline</h4>

<p>Student should maintain & promote cordial relation among the Teachers, NTS and themselves; Discipline must be maintained in the College campus. Loitering or indulging in noisy & disorderly conduct in the corridors and front of the office is strictly prohibited.</p>

<p>Students are expected to attend all the classes arranged for them in order to have the requisite percentage of attendance prescribed by the University for being eligible for University Examinations.</p>

<p>Irregular attendance, misbehavior, damaging College properties & adopting unfair means in Exam. Hall attracts disciplinary measures against the concerned students.</p>

