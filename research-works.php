<h4>Title of this Project</h4>
<p>WASTELAND AND STRATEGY FOR ITS MANAGEMENT THROUGH SOME PHYSICAL AND SOCIAL FACTORS IN THE SOUTHERN PART OF DEOGHAR DISTRICT, JHARKHAND.</p>


<h4>Findings of this Project</h4>
<p>The study attempts to analyse in detail all general and specific components, essential for wasteland classification and evolution, which can play an important role in making any conservation and reclamation programme by providing necessary informations that can also make these wastelands productive than now. It was observed that waste land increased to 59.60% of the total areas. In some places waste land increased as much as 10% to 15% with in 20% area of the total geographical areas due to mismanagement and lack of awareness.</p>

<p>This land can be converted into pasture land for farming of live stocks, fuel wood production and dry land farming. Although agricultural income is dominating, other employment generation programme are also needed for sustainable development with the help of wasteland management.</p>

<h4>This region needs special attention like :</h4>
<p>Agricultural loan provided to the farmers should be adequate and timely in the form of K C C, but proper planning and awareness of local people are equally very essential for plot to plot wasteland management. </p>

