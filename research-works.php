<h4>Title of this Project</h4>
<p>POVERTY ALLEVIATION THROUGH MICROFINANCE IN SOME PARTS OF BURDWAN DISTRICT, WEST BENGAL</p>
<h4>Focal theme of this Project</h4>
<p>Microfinance through SHG(Self Help Group) has demonstrated its potential for delivering a full range of commercial financial services to micro-entrepreneurs and poor families that are conventionally considered ‘‘unbankable’’ due to high transaction costs, perceived risks and low margins. Therefore, different Government schemes (through microfinance) designed for poverty alleviation among poor’s have been successful somewhere, but do not take into account the diversities of situation at the <strong>grass-root</strong> level (Drawing from research in interior rural areas of some parts of Burdwan District of West Bengal). This study aims at the viability of micro-finance and group approach as poverty alleviating strategies.</p>

