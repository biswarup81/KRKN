     <h4>Title of this Project</h4>
<p>POVERTY AND MICROFINANCE: INTERROGATING SELF-HELP GROUPS EXPERIENCE FROM PURI DISTRICT, OF ORISSA </p>

<div class="clearfix"></div>
<h4>Focal theme and Findings of this Project</h4>
<p>Development interventions are of many types and are implemented through different ways. Group approach has already been established as one of the potential tools. The success of group approach in rural micro-finance through SHG (Self Help Group) among the poor has inspired the tendency to look at all networking as essential and desirable in rural community development. This research aims to explore the impact of socio-economic condition on accumulation of social capital through participation in SHGs and pre-membership of SHGs under some Block of Puri District in Orissa and this research also justified the two way relationship between accumulation of social capital and effectiveness of SHGs in empowering its members economically & socially in this region. The study shows that there is a positive relationship between the years of association of the SHG member and the social empowerment (case of some parameter) which is created with the help of social capital. Micro finance organizations as well as government regulatory bodies both play supportive and innovative approach which are helping the existing financial condition but some time, in various cases regulatory bodies can not play supportive approach for development and poverty eradication. </p>

