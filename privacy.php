        <h2 class="featurette-heading text-gradient">Privacy  Policy</h2>
       

     <div class="conten-block clearfix">
      <div class=" blockquote">This Privacy Policy sets out the approach which Kandra Radhakanta Kundu Mahavidyalaya will take in relation to the treatment of Personal Information.  </div>
    
    
<h4> Collection</h4>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya will only collect Personal Information where the information is necessary for Kandra Radhakanta Kundu Mahavidyalaya to perform one or more of its functions or activities. In this context, "collect" means gather, acquire or obtain by any means, information in circumstances where the individual is identifiable or identified. Kandra Radhakanta Kundu Mahavidyalaya collects Personal Information primarily to supply customers with the products and services ordered from it and its related companies. Kandra Radhakanta Kundu Mahavidyalaya also collects and uses Personal Information for secondary purposes including billing and account management. Kandra Radhakanta Kundu Mahavidyalaya will not collect Sensitive Information from individuals except with consent and only where it is necessary for Kandra Radhakanta Kundu Mahavidyalaya to collect such information for an activity or function.</p>
 
<h4>Use</h4>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya will obtain an individual's consent for use of non-sensitive Personal Information for Secondary Purposes at the time of collection, unless the Use is a related Secondary Purpose which would be within Kandra Radhakanta Kundu Mahavidyalaya Uses Personal Information primarily for the purposes listed ABOVE.</p>
 
<p>Information primarily for the purposes listed ABOVE.</p>
<p>If Kandra Radhakanta Kundu Mahavidyalaya relies on the Direct Marketing exception to Direct Market to individuals it will ensure that: the individual is clearly notified of their right to Opt Out from further Direct Marketing; and if the individual Opts Out of all Direct Marketing the Opt Out will be respected by Kandra Radhakanta Kundu Mahavidyalaya</p>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya will not use Sensitive Information for Direct Marketing.</p>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya will not use Personal Information without taking reasonable steps to ensure that the information is accurate, complete and up to date.</p>
 
<h4>Disclosure</h4>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya may Disclose Personal Information to unrelated third parties to enable outsourcing of functions (such as billing, customer relations management and order fulfillment), where that is Disclosure or Use for a related Secondary Purpose and has been notified to individuals or where such Disclosure is within the individual's Reasonable Expectations.</p>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya will take reasonable steps to ensure that its contracts with third parties include requirements for third parties to comply with the Use and Disclosure requirements of the Privacy Act.</p>
 
<p>Kandra Radhakanta Kundu Mahavidyalaya may Disclose Personal Information to law enforcement agencies, government agencies, courts or external advisers where permitted or required by law.</p>

 
